package logica;

/**
 * Enum que representa las diferentes pruebas disponibles en el sistema.
 * Cada valor corresponde a una prueba de conocimiento que un cliente puede reservar.
 */
public enum Prueba {
    /**
     * Prueba de Matematica, nivel M1.
     */
    MATEMATICA_M1,

    /**
     * Prueba de Matematica, nivel M2.
     */
    MATEMATICA_M2,

    /**
     * Prueba de Competencia Lectora.
     */
    COMPETENCIA_LECTORA,

    /**
     * Prueba de Ciencias Fisicas.
     */
    CIENCIAS_FISICA,

    /**
     * Prueba de Ciencias Quimicas.
     */
    CIENCIAS_QUIMICA,

    /**
     * Prueba de Ciencias Biologicas.
     */
    CIENCIAS_BIOLOGIA,

    /**
     * Prueba de Historia y Ciencias Sociales.
     */
    HISTORIA_Y_CIENCIAS_SOCIALES
}